      <h3 class="texto-sobre-nos">O ano era 1996. O lugar, o interior de São Paulo, mais precisamente em Ribeirão Preto. Terra do chope, do café e do calor. Tem quem chame de Califórnia brasileira. Mas praia, não tem não. Enquanto isso, eu era só o Marcelo Carneiro, carioca chegando em Ribeirão, empresário do ramo farmacêutico. Mal sabia que a decisão de investir o meu tempo em outra paixão ia me transformar no “Marcelo da Colorado”. Escolhi trabalhar com algo que não remedia, mas sim que previne doenças: cerveja, amigos e diversão. Assim nasceu a Colorado. Com um sorriso no rosto e a paixão em fazer cerveja.

A Cervejaria abriu as portas não com um, mas com oito chopes diferentes. Alto teor alcoólico, lupúlados, não ﬁltrados, com ingredientes brasileiros.Tinha de tudo. Se é inusitado hoje, imagina em 1996, no interior de São Paulo. Teve gente que me chamou de louco. Mas sempre tive esse espírito Quixotesco, de viajar para explorar o mundo, mas que também volta pra casa e divide o que viu.

Foi nessa mistura de olhar pra fora com os pés no interior que a Colorado foi desbravando o seu caminho. Bebendo na fonte de estilos clássicos e fazendo releituras com ingredientes brasileiros.

Nossa pequena cervejaria foi colecionando prêmios pelo mundo. Cervejeiros respeitados tomavam um avião até Ribeirão só para beber Colorado e conhecer nossa casa. Daí pra frente, mais e mais pessoas foram provocadas a despertar para novos jeitos de beber cerveja. A ir além. Foi uma longa caminhada. 10 anos se passaram. Em 2005, chamei um pequeno, mas apaixonado, movimento local cervejeiro, e chegamos a conclusão de que só havia um caminho para a Colorado continuar crescendo e chegar a mais pessoas: ir além novamente. A Cervejaria deixou de produzir só chopes, foi para as garrafas vidro e batizamos nossas cervejas de trigo, Porter e India Pale Ale. A Colorado passou a ser uma marca. Até que de repente, meio sem aviso, chegou o urso que conhecemos. Por sinal, o urso pode ser considerado ﬁlho do Randy Mosher, um amigo de velhos tempos, respeitado cervejeiro e designer premiado. O Randy pintou o urso e os rótulos a mão, inspirado em referências brasileiras misturadas com estilos clássicos, de anos 50 a uma pitada de art deco. Uma metáfora visual do espírito explorador da Colorado, sem dogmas, que bebe do passado mas olha para o futuro. Saindo de 1996, Ribeirão Preto, interior de São Paulo, e indo além dos limites do código de área em direção a um mundo de possibilidades.

As próximas páginas deste livro vão te levar a desbravar o território da Colorado, conhecer cada detalhe do habitat natural do urso e despertar para um jeito criativo, e bem brasileiro, de fazer cerveja. Pronto para desibernar? Abraços, Marcelo Carneiro – Fundador da Cervejaria Colorado</h3>
